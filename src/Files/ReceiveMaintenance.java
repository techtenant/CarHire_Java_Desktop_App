 private void searchVehicle() {
        String query = "select * from `hire`.`vehicle` WHERE status='Under Maintenance'";
        try {
            ps = con.prepareStatement(query);
            rs = ps.executeQuery();
            while (rs.next()) {

                String reg = String.valueOf(rs.getString("Reg_No"));
                cmbreg.addItem(reg);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR",
                    JOptionPane.ERROR_MESSAGE);

        }

    }

    private void FreeVehicle() {
        String query = "UPDATE `hire`.`vehicle` SET status='Free' WHERE Reg_No='"
                + cmbreg.getSelectedItem() + "'";
        try {
            ps = con.prepareStatement(query);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Vehicle Freed","SUCCESS",
                    JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(),"ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
    }
