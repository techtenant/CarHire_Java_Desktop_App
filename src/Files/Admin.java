    private void delete() {
        String d = JOptionPane.showInputDialog(null, "Enter Username");
        String Query = "SELECT * FROM `login`";
        try {
            ps = conn.prepareStatement(Query);
            rs = ps.executeQuery();
            if (rs.next()) {
                String code = String.valueOf(rs.getString("Username"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        }
        if (d.isEmpty()) {
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Delete Failed. You Must Enter the Username.", "ERROR",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                int p = JOptionPane.showConfirmDialog(null, "Are sure you want to delete?", "Delete Dialog",
                        JOptionPane.YES_NO_OPTION);
                if (p == 0) {
                    String delete = "DELETE FROM `hire`. `login` WHERE username = '" + d + "'";
                    ps = conn.prepareStatement(delete);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "User Deleted Successfully.", "Delete Dialog",
                            JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Record Not Deleted Successfully.".concat(e.getMessage()),
                        "Delete Dialog", JOptionPane.ERROR_MESSAGE);
                Toolkit.getDefaultToolkit().beep();

            }
        }

    }
